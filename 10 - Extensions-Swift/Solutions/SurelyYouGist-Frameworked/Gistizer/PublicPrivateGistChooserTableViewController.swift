import UIKit

class PublicPrivateGistChooserTableViewController: UITableViewController {

    var isPublic: Bool = true
    var completion: ((_ isPublic: Bool) -> Void)? = nil

    let kPrivateIndex = 0
    let kPublicIndex = 1
    
    override func viewDidLoad() {
        super.viewDidLoad()
        self.title = "Gist Privacy"
    }
    
    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }
    
    // MARK: - Table view data source


    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        
        let cell = super.tableView(tableView, cellForRowAt: indexPath)

        if isPublic && (indexPath as NSIndexPath).row == kPublicIndex
            || !isPublic && (indexPath as NSIndexPath).row == kPrivateIndex {
                cell.accessoryType = .checkmark
        } else {
            cell.accessoryType = .none
        }
        return cell
    }
    
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        isPublic = (indexPath as NSIndexPath).row == kPublicIndex
        tableView.reloadData()
        
        completion?(isPublic)
    }
    
 
